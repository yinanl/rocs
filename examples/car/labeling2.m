function p = labeling2(x, G)
if(x(1)>=G{3}(1,1) && x(1)<=G{3}(1,2) &&...
        x(2)>=G{3}(2,1) && x(2)<=G{3}(2,2))
    p= 32; % a1
elseif(x(1)>=G{6}(1,1) && x(1)<=G{6}(1,2) &&...
        x(2)>=G{6}(2,1) && x(2)<=G{6}(2,2))
    p= 1; % a4
elseif(x(1)>=G{1}(1,1) && x(1)<=G{1}(1,2) &&...
        x(2)>=G{1}(2,1) && x(2)<=G{1}(2,2))  % c
    if(x(1)>=G{4}(1,1) && x(1)<=G{4}(1,2) &&...
            x(2)>=G{4}(2,1) && x(2)<=G{4}(2,2))
        p= 10; % a2
    else
        p= 8; % c
    end
elseif(x(1)>=G{2}(1,1) && x(1)<=G{2}(1,2) &&...
        x(2)>=G{2}(2,1) && x(2)<=G{2}(2,2))
    if(x(1)>=G{5}(1,1) && x(1)<=G{5}(1,2) &&...
            x(2)>=G{5}(2,1) && x(2)<=G{5}(2,2))
        p= 20; % a3
    else
        p= 4; % d
    end
else
    p= 0;
    
end